// 五行对应天干地支周期（简化版：用日期%5映射）
var WUXING_CYCLE = ['wood', 'fire', 'earth', 'metal', 'water'];
var WUXING_NAMES = { wood: '木', fire: '火', earth: '土', metal: '金', water: '水' };
var WUXING_EMOJIS = { wood: '🌿', fire: '🔥', earth: '🌾', metal: '🌙', water: '🌊' };
var WUXING_COLORS = { wood: '#2D8B55', fire: '#C9544D', earth: '#C6983A', metal: '#8C8C98', water: '#2E6B9E' };

function getTodayElement() {
    var now = new Date();
    var dayOfYear = Math.floor((now - new Date(now.getFullYear(), 0, 0)) / 86400000);
    return WUXING_CYCLE[dayOfYear % 5];
}

function getTodaySign(userElement) {
    var todayEl = getTodayElement();
    var signs = DAILY_SIGNS[userElement || todayEl];
    var now = new Date();
    var idx = (now.getDate() + now.getMonth()) % signs.length;
    return {
        todayElement: todayEl,
        todayName: WUXING_NAMES[todayEl],
        todayEmoji: WUXING_EMOJIS[todayEl],
        todayColor: WUXING_COLORS[todayEl],
        sign: signs[idx],
        dateStr: (now.getMonth() + 1) + '月' + now.getDate() + '日',
        weekday: ['日', '一', '二', '三', '四', '五', '六'][now.getDay()]
    };
}

